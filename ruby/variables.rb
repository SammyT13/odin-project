# Variables

## Declaring a Variable

age = 18
p age

    ## Assign Expressions to a Variable
age = 18 + 5
p age

    ## Reassign a Variable
age = age + 4
p age

    ## Shorthand Assignment Operatiors
        ## Example: +=, -=, *=, and /=
age += 4
p age

age -= 2
p age

cash = 10
cash *= 2
p cash

temp = 40
temp /= 10
p temp

